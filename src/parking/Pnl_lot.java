    public ResultSet countlot(){
	ResultSet rs_lot = null;
	try{
	String sql = "SELECT * FROM `parkinglot`";
	    Connection con = MyConnection.getConnection();
	    PreparedStatement pst = con.prepareStatement(sql);
	    rs_lot = pst.executeQuery();
	    while(rs_lot.next()){
		jlabel[count] = new JPanel();
		text[count] = new JLabel();
		jlabel[count].setName(rs_lot.getString("name"));
		jlabel[count].add(text[count]);
		jlabel[count].setSize(60, 100);
		jlabel[count].setBackground(new Color(153,153,153));
		panel1.add(jlabel[count]);
		jlabel[count].setLocation(Integer.parseInt(rs_lot.getString("positionx")), Integer.parseInt(rs_lot.getString("positiony")));
		jlabel[count].addMouseListener(this);
		jlabel[count].addMouseMotionListener(this);
		text[count].setText(rs_lot.getString("name"));
		count++;
	    }
	}catch(Exception e){
	    System.out.println(e);
	}
	return rs_lot;
    }
    
