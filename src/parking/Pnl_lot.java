    private void bn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bn_addActionPerformed

	    String name = JOptionPane.showInputDialog(null, "Lot name?");
	    if(checkduplicate(name) == true){
		JOptionPane.showMessageDialog(null, "Already have this parking lot name");
	    }else if(name.equals("")){
		JOptionPane.showMessageDialog(null, "Please enter parking lot name");
	    }else{
		jlabel[count] = new JPanel();
		text[count] = new JLabel();
		jlabel[count].setName(name);
		jlabel[count].add(text[count]);
		jlabel[count].setSize(60, 100);
		jlabel[count].setBackground(new Color(153,153,153));
	        panel1.add(jlabel[count]);
		jlabel[count].setLocation(318, 221);
		jlabel[count].addMouseListener(this);
		jlabel[count].addMouseMotionListener(this);
		createlot(0, 50, name);
		text[count].setText(name);
		count++;
	    }
    }//GEN-LAST:event_bn_addActionPerformed

    private void bn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bn_saveActionPerformed
        for(int i = 0; i < count; i++){
	    int x = jlabel[i].getX();
	    int y = jlabel[i].getY();
	    String name = jlabel[i].getName();
	savePosition(x,y,name);
	}
    }//GEN-LAST:event_bn_saveActionPerformed

    public void shiftlist(int j){
	for(int i = j; i < count; i++){
	    jlabel[i] = jlabel[i+1];
	}
    }
    
    private void bn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bn_deleteActionPerformed
        String name = JOptionPane.showInputDialog(null, "What is Lot name is you want to delete?");
	int check = 0;
	for(int i = 0;i< count; i++){
	    String c_name = jlabel[i].getName();
	    System.out.println(c_name);
	    if(c_name.equals(name)){
		jlabel[i].setVisible(false);
		check = 1;
		shiftlist(i);
		count--;
		deletelot(name);
	    }
	}
	if(check == 0){
	    JOptionPane.showMessageDialog(null, "Not found");
	}
    }//GEN-LAST:event_bn_deleteActionPerformed


